#!/usr/bin/python3

def soma(x,y):
    return x + y

def subtrai(x,y):
    return x * y

def multiplic(x,y):
    return x * y

def divisao(x,y):
    return x / y



assert soma(1,1) == 2, 'O modulo de soma não está configurado corretamente'
assert subtrai(2,1) == 1, 'O modulo de subtração não está configurado corretamente'
assert multiplic(3,2) == 6, 'O modulo de multiplicação não está configurado corretamente'
assert divisao(8,4) == 2, 'O modulo de divisão não está configurado corretamente'